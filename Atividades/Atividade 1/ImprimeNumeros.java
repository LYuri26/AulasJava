public class ImprimeNumeros {
    public static void main(String[] args) {
        // Loop for para imprimir números de 1 a 10
        System.out.println("Números de 1 a 10:");
        for (int i = 1; i <= 10; i++) {
            System.out.println(i);
        }

        // Loop for para imprimir números de 10 a 1
        System.out.println("Números de 10 a 1:");
        for (int i = 10; i >= 1; i--) {
            System.out.println(i);
        }
    }
}
